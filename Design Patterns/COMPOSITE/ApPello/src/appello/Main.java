    /*
    When the button "Generate HTML" is triggered, 
    the software had analyzed which RadioButtons station selected 
    and then will be generated HTML code.
    */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if (jRadioButton1.isSelected()) {
                Radio radio = HTMLcode.obj3();
                print = radio.print();
            }
            if (jRadioButton2.isSelected()) {
                Textfield textfield = HTMLcode.obj5();
                print = textfield.print();
            }
            if (jRadioButton3.isSelected()) {
                aux_items = jTextField3.getText();
                items = Integer.parseInt(aux_items);
                Combo combo = HTMLcode.obj7(items);
                print = combo.print();
            }
            if (jRadioButton4.isSelected()) {
                Checkbox checkbox = HTMLcode.obj2();
                print = checkbox.print();
            }
            if (jRadioButton5.isSelected()) {
                col = jTextField8.getText();
                str_row = jTextField7.getText();
                Textarea textarea = HTMLcode.obj1(col, str_row);
                print = textarea.print();
            }
            if (jRadioButton6.isSelected()) {
                aux_row = jTextField5.getText();
                aux_cel = jTextField6.getText();
                int_row = Integer.parseInt(aux_row);
                cel = Integer.parseInt(aux_cel);
                Table table = HTMLcode.obj8(int_row, cel);
                print = table.print();
            }
            if (jRadioButton7.isSelected()) {
                Button button = HTMLcode.obj4();
                print = button.print();
            }
            if (jRadioButton8.isSelected()) {
                name = jTextField1.getText();
                id = jTextField2.getText();
                num_method = jComboBox1.getSelectedIndex();
                if (num_method == 1) {
                    method = "GET";
                } else {
                    method = "POST";
                }
                target = jTextField4.getText();
                Form form = HTMLcode.obj6(name, id, method, target);
                print = form.print();
            }
            jTextArea1.setText("");
            jTextArea1.insert(print, 1);
            // TODO add your handling code here:
        }catch(java.lang.NumberFormatException ex){
            jTextArea1.setText("");
            jTextArea1.insert("Just Integer Numbers", 1);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

